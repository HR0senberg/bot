# Обработчик команды /start
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет! Я твой чат-бот. Напиши 'цитата', и я отправлю тебе вдохновляющую цитату."
    )

# Функция для генерации случайной цитаты
def generate_quote():
    return random.choice(QUOTES)

# Обработчик текстовых сообщений
async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_message = update.message.text.lower()  # Приводим сообщение к нижнему регистру

    if "цитата" in user_message:
        quote = generate_quote()
        await update.message.reply_text(quote)
    else:
        await update.message.reply_text("Напиши 'цитата', и я отправлю тебе вдохновляющую цитату.")

# Обработчик ошибок
async def error(update: Update, context: ContextTypes.DEFAULT_TYPE):
    print(f"Ошибка: {context.error}")

# Основная функция для запуска бота
def main():
    # Создаем приложение и передаем токен
    application = Application.builder().token(TOKEN).build()

    # Регистрируем обработчики команд
    application.add_handler(CommandHandler("start", start))

    # Регистрируем обработчик текстовых сообщений
    application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    # Регистрируем обработчик ошибок
    application.add_error_handler(error)

    # Запускаем бота
    print("Бот запущен...")
    application.run_polling()

if __name__ == "__main__":
    main()